package com.loop;

import java.util.Iterator;

public class Nested_mathi {
	
	public static void main(String[] args) {
		for (int i = 7; i <=10; i++) {// outer loop
			
			for (int j = 7; j <=i ; j++) {// inner l
			
				System.out.print(j);
			}
//			System.out.println();
			 
		}
		for (int k =4 ; k <=6 ; k++) {
			System.out.println(k);
		}
	}
	

}



